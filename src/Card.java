
public class Card {

	private String cardName;
	private CardType cardType;
	
	public String getCardName() {
		return cardName;
	}
	
	public void setCardName(String name) {
		this.cardName = name;
	}
	
	public CardType getCardType() {
		return cardType;
	}
	
	public void setCardType(CardType cardType) {
		this.cardType = cardType;
	}
}
